// تابع شروع روان‌خوانی و پخش موزیک روان‌خوانی
function startReading() {
  const sentenceElement = document.getElementById('sentence');
  sentenceElement.textContent = sentences[currentSentenceIndex];
  sentenceElement.style.left = '100%';

  // قطع موزیک پس‌زمینه و پخش موزیک روان‌خوانی
  const backgroundMusic = document.getElementById('background-music');
  backgroundMusic.pause();
  backgroundMusic.currentTime = 0;

  const calmMusic = document.getElementById('calm-music');
  calmMusic.play();

  animationInterval = setInterval(() => {
    const currentLeft = parseFloat(sentenceElement.style.left);
    if (currentLeft < -sentenceElement.offsetWidth) {
      sentenceElement.style.left = '100%';
    } else {
      sentenceElement.style.left = (currentLeft - speed) + 'px';
    }
  }, 100); // سرعت حرکت جمله کاهش یافته است
}

// تابع توقف روان‌خوانی و توقف موزیک روان‌خوانی
function stopReading() {
  clearInterval(animationInterval);

  // توقف موزیک روان‌خوانی
  const calmMusic = document.getElementById('calm-music');
  calmMusic.pause();
  calmMusic.currentTime = 0;
}

// تابع نمایش جمله بعدی
function nextSentence() {
  currentSentenceIndex = (currentSentenceIndex + 1) % sentences.length;
  document.getElementById('sentence').textContent = sentences[currentSentenceIndex];
}

// تابع به‌روزرسانی سرعت حرکت جمله
function updateSpeed() {
  speed = parseInt(document.getElementById('speed').value);
}

// تابع اضافه کردن جمله جدید
function addSentence() {
  const newSentence = document.getElementById('new-sentence').value;
  if (newSentence.trim() !== "") {
    sentences.push(newSentence);
    document.getElementById('new-sentence').value = ""; // پاک کردن فیلد ورودی
    alert("جمله جدید با موفقیت اضافه شد!");
  } else {
    alert("لطفاً یک جمله وارد کنید!");
  }
}

